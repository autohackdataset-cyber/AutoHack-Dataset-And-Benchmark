"""
train_38f.py
=============
Reads and models the saved 38-feature proc files.

Prerequisites: Execute `preprocessing38f.py` first to generate the following files:
  preprocess/source/AutoHack2025_38f/
    train_proc_38f.pkl
    test_proc_38f.pkl
    feature_columns.txt

Content:
  - Per-Interface training/evaluation (B-CAN, C-CAN, P-CAN)
  - Combined training followed by per-Interface breakdown
  - RF + XGBoost 
  - Result summary prints to console + text outputs in Result/observation3

Usage:
    python observation3.py
"""

import os
import glob
import pickle
import warnings
from datetime import datetime

import numpy as np
import matplotlib.pyplot as plt
from sklearn.ensemble import RandomForestClassifier
from sklearn.preprocessing import LabelEncoder, StandardScaler
from sklearn.metrics import (accuracy_score, precision_score, recall_score,
                             f1_score, confusion_matrix, classification_report)
import xgboost as xgb

warnings.filterwarnings('ignore')


# =============================================================================
# Configuration
# =============================================================================
BASE_PATH = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..")
PROC_DIR    = os.path.join(BASE_PATH, "preprocess", "source", "AutoHack2025_38f")
TRAIN_PKL   = os.path.join(PROC_DIR, "train_proc_38f.pkl")
TRAIN_PKL_B = os.path.join(PROC_DIR, "train_proc_b_38f.pkl")
TRAIN_PKL_C = os.path.join(PROC_DIR, "train_proc_c_38f.pkl")
TRAIN_PKL_P = os.path.join(PROC_DIR, "train_proc_p_38f.pkl")
TEST_PKL    = os.path.join(PROC_DIR, "test_proc_38f.pkl")
TEST_PKL_B  = os.path.join(PROC_DIR, "test_proc_b_38f.pkl")
TEST_PKL_C  = os.path.join(PROC_DIR, "test_proc_c_38f.pkl")
TEST_PKL_P  = os.path.join(PROC_DIR, "test_proc_p_38f.pkl")
FEAT_TXT    = os.path.join(PROC_DIR, "feature_columns.txt")

RESULT_DIR  = os.path.join(BASE_PATH, "Result", "observation3")
os.makedirs(RESULT_DIR, exist_ok=True)

INTERFACES  = ['B-CAN', 'C-CAN', 'P-CAN']
MODEL_TYPES = ['RF', 'XGBoost']
TOP_N_FEATURES = 15


# =============================================================================
# Model wrapper
# =============================================================================
class CANIDSModel:
    def __init__(self, model_type='RF'):
        self.model_type = model_type
        self.model = None

    def build_model(self):
        if self.model_type == 'RF':
            self.model = RandomForestClassifier(
                n_estimators=100,
                max_depth=20,
                min_samples_split=5,
                min_samples_leaf=2,
                random_state=42,
                n_jobs=-1,
            )
        elif self.model_type == 'XGBoost':
            self.model = xgb.XGBClassifier(
                n_estimators=100,
                max_depth=10,
                learning_rate=0.1,
                subsample=0.8,
                colsample_bytree=0.8,
                random_state=42,
                n_jobs=-1,
                eval_metric='mlogloss',
            )
        else:
            raise ValueError(f"Unsupported model: {self.model_type}")

    def train(self, X_train, y_train):
        print(f"  Training {self.model_type} ...")
        self.model.fit(X_train, y_train)
        print(f"  {self.model_type} done.")

    def predict(self, X_test):
        return self.model.predict(X_test)

    def evaluate(self, X_test, y_test):
        y_pred = self.predict(X_test)
        return {
            'accuracy':  accuracy_score(y_test, y_pred),
            'precision': precision_score(y_test, y_pred, average='weighted', zero_division=0),
            'recall':    recall_score(y_test, y_pred, average='weighted', zero_division=0),
            'f1_score':  f1_score(y_test, y_pred, average='macro', zero_division=0),
            'cm':        confusion_matrix(y_test, y_pred),
            'report':    classification_report(y_test, y_pred, zero_division=0),
            'y_pred':    y_pred,
        }

    def get_feature_importance(self, feature_columns):
        if self.model is None:
            return None

        if hasattr(self.model, 'feature_importances_'):
            importances = np.asarray(self.model.feature_importances_, dtype=float)
            return {
                'features': list(feature_columns),
                'importances': importances,
            }

        return None


# =============================================================================
# Data loading
# =============================================================================
def load_proc():
    """Load proc files generated by preprocess_39f.py"""
    print("=" * 80)
    print("LOADING PROC FILES")
    print("=" * 80)

    if not os.path.exists(TRAIN_PKL) or not os.path.exists(TEST_PKL):
        raise FileNotFoundError(
            f"\n  Opps, proc files are missing.\n"
            f"    Checked path: {PROC_DIR}\n"
            f"    Please execute 'python preprocessing38f.py' first.\n"
        )
    
    print(f"  loading {TRAIN_PKL} ...")
    with open(TRAIN_PKL, 'rb') as f:
        train_proc = pickle.load(f)
    print(f"    -> {train_proc.shape}")
    train_proc_interface = {}
    with open(TRAIN_PKL_B, 'rb') as f:
        train_proc_interface[INTERFACES[0]] = pickle.load(f)
    print(f"    -> {train_proc_interface[INTERFACES[0]].shape}")
    with open(TRAIN_PKL_C, 'rb') as f:
        train_proc_interface[INTERFACES[1]] = pickle.load(f)
    print(f"    -> {train_proc_interface[INTERFACES[1]].shape}")
    with open(TRAIN_PKL_P, 'rb') as f:
        train_proc_interface[INTERFACES[2]] = pickle.load(f)
    print(f"    -> {train_proc_interface[INTERFACES[2]].shape}")

    print(f"  loading {TEST_PKL} ...")
    with open(TEST_PKL, 'rb') as f:
        test_proc = pickle.load(f)
    print(f"    -> {test_proc.shape}")
    test_proc_interface = {}
    with open(TEST_PKL_B, 'rb') as f:
        test_proc_interface[INTERFACES[0]] = pickle.load(f)
    print(f"    -> {test_proc_interface[INTERFACES[0]].shape}")
    with open(TEST_PKL_C, 'rb') as f:
        test_proc_interface[INTERFACES[1]] = pickle.load(f)
    print(f"    -> {test_proc_interface[INTERFACES[1]].shape}")
    with open(TEST_PKL_P, 'rb') as f:
        test_proc_interface[INTERFACES[2]] = pickle.load(f)
    print(f"    -> {test_proc_interface[INTERFACES[2]].shape}")

    if os.path.exists(FEAT_TXT):
        with open(FEAT_TXT, 'r', encoding='utf-8') as f:
            feature_columns = [line.strip() for line in f if line.strip()]
    else:
        # fallback: derive from columns
        feature_columns = [c for c in train_proc.columns if c not in ('Interface', 'Label')]

    print(f"  features ({len(feature_columns)}): first 5 = {feature_columns[:5]}, ...")
    print(f"  train labels: {sorted(train_proc['Label'].unique())}")
    print(f"  test  labels: {sorted(test_proc['Label'].unique())}")
    print(f"  train per-bus: "
          + ", ".join(f"{b}={(train_proc['Interface'] == b).sum():,}" for b in INTERFACES))
    print(f"  test  per-bus: "
          + ", ".join(f"{b}={(test_proc['Interface'] == b).sum():,}" for b in INTERFACES))

    return train_proc, test_proc, feature_columns, train_proc_interface, test_proc_interface


# =============================================================================
# Per-interface training
# =============================================================================
def train_and_evaluate_interface(train_proc, test_proc, feature_columns, interface):
    print(f"\n{'=' * 80}")
    print(f"Processing {interface}")
    print(f"{'=' * 80}")

    X_train_raw = train_proc[feature_columns].values
    y_train = train_proc['Label'].values
    X_test_raw = test_proc[feature_columns].values
    y_test = test_proc['Label'].values

    print(f"  Train: {X_train_raw.shape}, Test: {X_test_raw.shape}")

    le = LabelEncoder()
    le.fit(np.concatenate([y_train, y_test]))
    y_train_enc = le.transform(y_train)
    y_test_enc  = le.transform(y_test)

    scaler = StandardScaler()
    X_train = scaler.fit_transform(X_train_raw)
    X_test  = scaler.transform(X_test_raw)

    results = {}
    for mt in MODEL_TYPES:
        print(f"\n  --- {mt} ---")
        model = CANIDSModel(mt)
        model.build_model()

        model.train(X_train, y_train_enc)

        m = model.evaluate(X_test, y_test_enc)
        m['feature_importance'] = model.get_feature_importance(feature_columns)
        print(f"    Acc:  {m['accuracy']:.4f}")
        print(f"    Prec: {m['precision']:.4f}")
        print(f"    Rec:  {m['recall']:.4f}")
        print(f"    Macro-F1: {m['f1_score']:.4f}")
        results[mt] = m

    return results


# =============================================================================
# Combined training
# =============================================================================
def train_and_evaluate_combined(train_proc, test_proc, feature_columns):
    print(f"\n{'=' * 80}")
    print("Processing Combined (All Interfaces)")
    print(f"{'=' * 80}")

    X_train_raw = train_proc[feature_columns].values
    y_train     = train_proc['Label'].values
    X_test_raw  = test_proc[feature_columns].values
    y_test      = test_proc['Label'].values

    print(f"  Train: {X_train_raw.shape}, Test: {X_test_raw.shape}")

    le = LabelEncoder()
    le.fit(np.concatenate([y_train, y_test]))
    y_train_enc = le.transform(y_train)
    y_test_enc  = le.transform(y_test)

    scaler = StandardScaler()
    X_train = scaler.fit_transform(X_train_raw)
    X_test  = scaler.transform(X_test_raw)

    results = {}
    for mt in MODEL_TYPES:
        print(f"\n  --- {mt} (Combined) ---")
        model = CANIDSModel(mt)
        model.build_model()

        model.train(X_train, y_train_enc)

        m = model.evaluate(X_test, y_test_enc)
        m['feature_importance'] = model.get_feature_importance(feature_columns)
        print(f"    Overall Acc: {m['accuracy']:.4f}, Macro-F1: {m['f1_score']:.4f}")

        # Per-interface breakdown
        breakdown = {}
        y_pred = m['y_pred']
        for iface in INTERFACES:
            iface_mask = (test_proc['Interface'] == iface).values
            if iface_mask.sum() == 0:
                continue
            yt = y_test_enc[iface_mask]
            yp = y_pred[iface_mask]
            breakdown[iface] = {
                'samples':   int(iface_mask.sum()),
                'accuracy':  accuracy_score(yt, yp),
                'precision': precision_score(yt, yp, average='weighted', zero_division=0),
                'recall':    recall_score(yt, yp, average='weighted', zero_division=0),
                'f1_score':  f1_score(yt, yp, average='macro', zero_division=0),
                'cm':        confusion_matrix(yt, yp),
            }
            b = breakdown[iface]
            print(f"    {iface}: n={b['samples']:>8,}  "
                  f"Acc={b['accuracy']:.4f}  Macro-F1={b['f1_score']:.4f}")

        m['interface_breakdown'] = breakdown
        results[mt] = m

    return results


# =============================================================================
# File numbering
# =============================================================================
def get_next_index(save_dir):
    prefixes = ['result_overview_', 'summary_']
    exts = ['.png', '.txt']
    indices = []

    for prefix, ext in zip(prefixes, exts):
        pattern = os.path.join(save_dir, f"{prefix}*{ext}")
        for path in glob.glob(pattern):
            base = os.path.basename(path)
            if not (base.startswith(prefix) and base.endswith(ext)):
                continue
            num_part = base[len(prefix):-len(ext)]
            try:
                indices.append(int(num_part))
            except ValueError:
                continue

    return (max(indices) + 1) if indices else 1


