def _plot_confusion_matrix(ax, cm, class_names, title):
    # Use lighter colormap and annotation bbox to improve contrast
    vmax = cm.max() if cm.size else 1
    im = ax.imshow(
        cm,
        aspect='auto',
        cmap='Blues',
        interpolation='nearest',
        vmin=0,
        vmax=vmax
    )
    ax.set_title(title, fontsize=11, pad=10)
    ax.set_xlabel('Predicted', fontsize=9)
    ax.set_ylabel('True', fontsize=9)

    ax.set_xticks(np.arange(len(class_names)))
    ax.set_yticks(np.arange(len(class_names)))
    ax.set_xticklabels(class_names, rotation=45, ha='right', fontsize=8)
    ax.set_yticklabels(class_names, fontsize=8)

    # Add cell outlines for better visual distinction
    ax.set_xticks(np.arange(-0.5, len(class_names), 1), minor=True)
    ax.set_yticks(np.arange(-0.5, len(class_names), 1), minor=True)
    ax.grid(which='minor', color='gray', linestyle='-', linewidth=0.35, alpha=0.35)
    ax.tick_params(which='minor', bottom=False, left=False)

    threshold = vmax * 0.55 if cm.size else 0
    for i in range(cm.shape[0]):
        for j in range(cm.shape[1]):
            value = cm[i, j]
            ax.text(
                j, i, f"{value}",
                ha='center', va='center',
                color='white' if value > threshold else 'black',
                fontsize=8,
                fontweight='bold',
                bbox=dict(
                    boxstyle='round,pad=0.18',
                    facecolor='white',
                    edgecolor='none',
                    alpha=0.72
                )
            )

    cbar = plt.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
    cbar.ax.tick_params(labelsize=8)


def _plot_feature_importance(ax, fi_dict, title, top_n=TOP_N_FEATURES):
    if not fi_dict or 'importances' not in fi_dict or len(fi_dict['importances']) == 0:
        ax.set_title(title, fontsize=10)
        ax.text(0.5, 0.5, 'Feature importance unavailable', ha='center', va='center')
        ax.axis('off')
        return

    features = np.array(fi_dict['features'])
    importances = np.array(fi_dict['importances'], dtype=float)
    order = np.argsort(importances)[::-1][:top_n]

    top_features = features[order][::-1]
    top_importances = importances[order][::-1]

    ax.barh(range(len(top_features)), top_importances)
    ax.set_yticks(range(len(top_features)))
    ax.set_yticklabels(top_features, fontsize=8)
    ax.set_title(title, fontsize=10)
    ax.set_xlabel('Importance')


def save_result_overview(interface_results, class_names, idx):
    plot_pairs = []

    for iface in INTERFACES:
        if iface not in interface_results:
            continue
        for mt in MODEL_TYPES:
            if mt not in interface_results[iface]:
                continue
            result = interface_results[iface][mt]
            plot_pairs.append({
                'cm_title': f'{iface} - {mt}\nConfusion Matrix',
                'cm': result['cm'],
                'fi_title': f'{iface} - {mt}\nFeature Importance',
                'fi': result.get('feature_importance'),
            })

    if 'Combined' in interface_results:
        for mt in MODEL_TYPES:
            if mt not in interface_results['Combined']:
                continue
            result = interface_results['Combined'][mt]
            plot_pairs.append({
                'cm_title': f'Combined - {mt}\nConfusion Matrix',
                'cm': result['cm'],
                'fi_title': f'Combined - {mt}\nFeature Importance',
                'fi': result.get('feature_importance'),
            })

    if not plot_pairs:
        return None

    nrows = len(plot_pairs)
    fig, axes = plt.subplots(nrows=nrows, ncols=2, figsize=(18, max(4.5 * nrows, 8)))

    if nrows == 1:
        axes = np.array([axes])

    for row_idx, pair in enumerate(plot_pairs):
        left_ax, right_ax = axes[row_idx]
        _plot_confusion_matrix(left_ax, pair['cm'], class_names, pair['cm_title'])
        _plot_feature_importance(right_ax, pair['fi'], pair['fi_title'])

    out = os.path.join(RESULT_DIR, f'result_overview_{idx:03d}.png')
    fig.tight_layout()
    fig.savefig(out, dpi=300, bbox_inches='tight')
    plt.close(fig)
    print(f"\n  saved figure -> {out}")
    return out


