# Save summary
# =============================================================================
def save_summary(interface_results, idx):
    out = os.path.join(RESULT_DIR, f"summary_{idx:03d}.txt")
    lines = []
    lines.append("=" * 80)
    lines.append("39-FEATURE TRAINING SUMMARY (F1 = Macro-F1)")
    lines.append("=" * 80)

    lines.append("\n[Per-Interface Separate Training]")
    for iface in INTERFACES:
        if iface not in interface_results:
            continue
        lines.append(f"\n  {iface}:")
        for mt in MODEL_TYPES:
            if mt not in interface_results[iface]:
                continue
            m = interface_results[iface][mt]
            lines.append(
                f"    {mt:<8} Acc={m['accuracy']:.4f}  "
                f"Prec={m['precision']:.4f}  Rec={m['recall']:.4f}  "
                f"Macro-F1={m['f1_score']:.4f}"
            )

    if 'Combined' in interface_results:
        lines.append("\n" + "=" * 80)
        lines.append("[Combined Training -> Per-Interface Test]")
        lines.append("=" * 80)
        for mt in MODEL_TYPES:
            if mt not in interface_results['Combined']:
                continue
            m = interface_results['Combined'][mt]
            lines.append(f"\n  {mt} (overall):")
            lines.append(
                f"    Acc={m['accuracy']:.4f}  Prec={m['precision']:.4f}  "
                f"Rec={m['recall']:.4f}  Macro-F1={m['f1_score']:.4f}"
            )
            if 'interface_breakdown' in m:
                lines.append(f"  {mt} (per-interface):")
                for iface in INTERFACES:
                    if iface not in m['interface_breakdown']:
                        continue
                    b = m['interface_breakdown'][iface]
                    lines.append(
                        f"    {iface:<7} n={b['samples']:>8,}  "
                        f"Acc={b['accuracy']:.4f}  Prec={b['precision']:.4f}  "
                        f"Rec={b['recall']:.4f}  Macro-F1={b['f1_score']:.4f}"
                    )

    text = "\n".join(lines)
    with open(out, 'w', encoding='utf-8') as f:
        f.write(text)
    print(f"\n  saved -> {out}")
    return text


# =============================================================================
# Main
# =============================================================================
def main():
    total_t0 = datetime.now()

    train_proc, test_proc, feature_columns, train_proc_interface, test_proc_interface = load_proc()

    interface_results = {}
    for iface in INTERFACES:
        try:
            interface_results[iface] = train_and_evaluate_interface(
                train_proc_interface[iface], test_proc_interface[iface], feature_columns, iface
            )
        except Exception as e:
            print(f"\n❌ Error processing {iface}: {e}")
            import traceback
            traceback.print_exc()

    try:
        interface_results['Combined'] = train_and_evaluate_combined(
            train_proc, test_proc, feature_columns
        )
    except Exception as e:
        print(f"\n❌ Error processing Combined: {e}")
        import traceback
        traceback.print_exc()

    idx = get_next_index(RESULT_DIR)
    text = save_summary(interface_results, idx)

    all_labels = sorted(set(train_proc['Label'].unique()) | set(test_proc['Label'].unique()))
    save_result_overview(interface_results, all_labels, idx)

    print("\n" + "=" * 80)
    print("FINAL SUMMARY")
    print("=" * 80)
    print(text)

    print(f"\nTotal time: {datetime.now() - total_t0}")


if __name__ == "__main__":
    main()
